#!/bin/bash

# 1. 設定日誌檔案路徑 (請確認你的系統路徑，測試時可改為當前目錄的 access.log)
LOG_FILE="/var/log/nginx/access.log"

# 2. 檢查日誌檔是否存在
if [ ! -f "$LOG_FILE" ]; then
    echo "❌ 錯誤：找不到日誌檔案 $LOG_FILE"
    echo "請使用 sudo 執行，或確認路徑是否正確。"
    exit 1
fi

echo "==================================================="
echo " 🕵️  Nginx 惡意請求分析報告 "
echo " 掃描目標: $LOG_FILE"
echo "==================================================="

# ---------------------------------------------------------
# 分析 1：尋找異常的 PHP 探測 (PHP Probing)
# ---------------------------------------------------------
echo -e "\n[1] 🚨 尋找狂掃 .php 檔案的惡意 IP (前 5 名):"
echo "---------------------------------------------------"
# 邏輯：用 grep 找 .php，擷取第1欄(IP)與第9欄(狀態碼)，只印出IP來計算次數
grep -i "\.php" "$LOG_FILE" | awk '{print $1}' | sort | uniq -c | sort -nr | head -n 5 | while read count ip; do
    echo "  -> IP: $ip (共發送了 $count 次 .php 請求)"
done

# ---------------------------------------------------------
# 分析 2：尋找路徑穿越攻擊 (Path Traversal / Local File Inclusion)
# ---------------------------------------------------------
echo -e "\n[2] ☠️ 尋找使用 ./ 或 ../ 的路徑穿越攻擊 (前 5 名):"
echo "---------------------------------------------------"
# 邏輯：用 grep -E 使用正則表達式，同時比對 ./ 或 ../
grep -E "\./|\.\./" "$LOG_FILE" | awk '{print $1}' | sort | uniq -c | sort -nr | head -n 5 | while read count ip; do
    echo "  -> IP: $ip (共嘗試了 $count 次路徑穿越)"
done

# ---------------------------------------------------------
# 分析 3：最危險的警報！攻擊是否成功？(狀態碼為 200 的惡意請求)
# ---------------------------------------------------------
echo -e "\n[3] 🔥 極度危險：疑似攻擊成功的請求 (回傳 200 OK):"
echo "---------------------------------------------------"
# 邏輯：同時包含 .php 或 ./ 且 狀態碼是 200 的請求
grep -E "\.php|\./|\.\./" "$LOG_FILE" | awk '$9 == 200 {print $1, $7}' | sort | uniq -c | sort -nr | head -n 5 | while read count ip uri; do
    echo "  [危險!] IP: $ip 成功讀取了: $uri ($count 次)"
done

echo -e "\n==================================================="
echo " 報告結束。若 [3] 區塊有出現資料，請立即封鎖該 IP！"
echo "==================================================="
